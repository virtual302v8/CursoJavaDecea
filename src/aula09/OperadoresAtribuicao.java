package aula09;

public class OperadoresAtribuicao {

	public static void main(String[] args) {
		
		
		int valor1 = 20;
		int valor2 = 7 + valor1;
		
		System.out.println(valor2);
		
		int atribuicao = 10;
		
		atribuicao += 10;
		
		System.out.println(atribuicao);
		
		atribuicao *= 2;
		
		System.out.println(atribuicao);

	}

}
